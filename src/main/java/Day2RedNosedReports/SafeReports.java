    public static boolean isReportSafe(int[] levels) {
        boolean isIncreasing = true;
        boolean isDecreasing = true;

        for (int i = 0; i < (levels.length - 1); i++) {
            int difference = Math.abs(levels[i + 1] - levels[i]);

            if (difference < 1 || difference > 3) {
                return false;
            }

            if (levels[i + 1] < levels[i]) {
                isIncreasing = false;
            }

            if (levels[i + 1] > levels[i]) {
                isDecreasing = false;
            }
        }
        return isIncreasing || isDecreasing;
    }

    public static boolean isReportSafeByRemovingOneLevel(int[] levels) {
        for (int i = 0; i < levels.length; i++) {
            int[] newLevels = new int[levels.length - 1];
            int index = 0;
            for (int j = 0; j < levels.length; j++) {
                if (j != i) {
                    newLevels[index++] = levels[j];
                }
            }

            if (isReportSafe(newLevels)) {
                return true;
            }
        }
        return false;
    }

